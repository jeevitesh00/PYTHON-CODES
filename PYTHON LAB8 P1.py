#Create a new set of identical items from two sets
# Creating two sets
set1 = {10, 20, 30, 40, 50}
set2 = {30, 40, 50, 60, 70}

# Creating a new set of identical items (intersection of the two sets)
identical_items = set1.intersection(set2)

# Printing the identical items
print("Identical items from both sets:", identical_items)

